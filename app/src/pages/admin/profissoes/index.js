import Axios from 'axios'
import NavAdmin from '@/components/NavAdmin'
import ProfessionAction from '@/components/ProfessionAction'  // Ajuste o nome do componente de ação
import Head from 'next/head'
import { useEffect, useState } from 'react'
import Link from 'next/link'
import MenuUsers from '@/components/MenuUsers'

export default function idProfissoes() {

  const API_URL = "http://localhost:8080/api/profissoes"  // URL de profissionais
  
  const [profissoes, setProfissoes] = useState([]); 
  
  useEffect(() => {
    const getAllProfissoes = async () => {
      try {
        const response = await Axios.get(API_URL);
        setProfissoes(response.data);
        console.log(response.data);
      } catch (error) {
        console.error('Erro ao buscar as profissoes:', error);
      }
    };

    getAllProfissoes();

  }, []);

  return (
    <>
      <Head>
        <title>APP-BC</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
      </Head>
      <div>
        <NavAdmin />
        <MenuUsers />
      </div>
  
      <div className="d-flex justify-content-center p-4" style={{ background: 'linear-gradient(45deg, #ffffff, #d4edda)', minHeight: '100vh'}}>
        <div className="container">
          <div className="row mb-4 pb-3" style={{ borderBottom: '1px solid #a0a0a0' }}>
            <h3 className="text-success">Lista de Profissoes</h3>
          </div>
  
          <div className="d-flex justify-content-center mb-4">
            <Link className="nav-link" href="/admin/profissoes/create">
              <button className="btn btn-success btn-lg p-3">Cadastrar</button>
            </Link>
          </div>
  
          <div className="row">
            {profissoes.map(profissao => (
              <div key={profissao.id} className="col-md-4 mb-4" id={`profissao-${profissao.id}`}>
                <div className="card shadow-lg border-success">
                  <div className="card-body">
                    <h5 className="card-title text-success">{profissao.nome}</h5>
                    <p className="card-text"><strong>Descrição: </strong> {profissao.descricao}</p>
                    <div className="d-flex justify-content-end">
                      <ProfessionAction pid={profissao.id} />
                    </div>
                  </div>
                </div>
              </div>
            ))}
          </div>
        </div>
      </div>
    </>
  );  
}
